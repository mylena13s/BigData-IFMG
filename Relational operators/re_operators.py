# EXEMPLOS

n1 = int(input('Informe um número: '))
n2 = int(input('Informe outro número: '))
print(n1, '==', n2, '->', n1 == n2)
print(n1, '!=', n2, '->', n1 != n2)
print(n1, '<-', n2, '->', n1 < n2)
print(n1, '<=', n2, '->', n1 <= n2)
print(n1, '>', n2, '->', n1 > n2)
print(n1, '>=', n2, '->', n1 >= n2)
